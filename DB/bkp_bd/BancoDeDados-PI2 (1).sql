CREATE TABLE PESSOA(
ID_PESSOA INTEGER(4) NOT NULL UNIQUE,
NOME VARCHAR(500) NOT NULL,
PRIMARY KEY (ID_PESSOA) 
);

CREATE TABLE CLIENTE(
ID_PESSOA INTEGER(4) NOT NULL UNIQUE,
CPF VARCHAR (14) NOT NULL UNIQUE,
IDADE INTEGER NOT NULL,
TELEFONE VARCHAR(11) NOT NULL,
RG VARCHAR(9) NOT NULL,
PRIMARY KEY (CPF),
FOREIGN KEY(ID_PESSOA) REFERENCES PESSOA(ID_PESSOA)
);

CREATE TABLE FUNCIONARIO(
ID_PESSOA INTEGER(4) NOT NULL UNIQUE,
ID_FUNCIONARIO INTEGER(2) NOT NULL UNIQUE,
PRIMARY KEY (ID_FUNCIONARIO),
FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID_PESSOA)
);

CREATE TABLE MOVIMENTACAO(
ID_MOVIMENTACAO INTEGER(5) UNIQUE,
ID_FUNCIONARIO INTEGER(2) UNIQUE,
CPF VARCHAR(14) NOT NULL UNIQUE,
ID_ITEM INTEGER UNIQUE,
DATA_MOV VARCHAR(10) NOT NULL,
STATUS VARCHAR(1) NOT NULL,
PRIMARY KEY (ID_MOVIMENTACAO),
FOREIGN KEY (ID_FUNCIONARIO) REFERENCES FUNCIONARIO(ID_FUNCIONARIO),
FOREIGN KEY (CPF) REFERENCES CLIENTE(CPF),
FOREIGN KEY (ID_ITEM) REFERENCES ITEM(ID_ITEM)
);

CREATE TABLE ITEM(
ID_ITEM INTEGER NOT NULL UNIQUE,
NOME VARCHAR(500) NOT NULL,
PRIMARY KEY (ID_ITEM)
);

CREATE TABLE SAPATOS(
ID_ITEM INTEGER UNIQUE,
ID_SAPATO INTEGER NOT NULL,
NUMERO INTEGER NOT NULL,
STATUS VARCHAR(100),
PRIMARY KEY (ID_SAPATO)
);

CREATE TABLE LUVAS(
ID_ITEM INTEGER NOT NULL UNIQUE,
ID_LUVA INTEGER NOT NULL,
TAMANHO INTEGER NOT NULL,
STATUS VARCHAR(100),
PRIMARY KEY (ID_LUVA)
);

